#/bin/env bash
################################################################################
# setup dotfile symlinks
################################################################################

FILES=`ls -I README -I setup.sh`

echo "Adding dotfile symlinks..."

for f in ${FILES}; do
    if [ ! -L ../.${f} ]; then
        echo "Adding link for: ~/.${f}"
        ln -s dotfiles/${f} ../.${f}
    fi
done

echo "Done."

